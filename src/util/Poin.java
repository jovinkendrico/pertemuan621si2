package util;

public class Poin {
    private int x, y;

    public Poin(int x, int y) {
        this.x = x;
        this.y = y;
    }
    

    public Poin() {
    }


    public int getX() {
        return this.x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return this.y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
}
